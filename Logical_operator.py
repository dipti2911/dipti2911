a=20
b=30
# Logical and operator gives true if both are true
print((a==20 and b==30))
print((a==39 and b==30))
# Logical or operator gives true if any one is true
print((a==20 or b==30))
print((a==23 or b==31))
# Logical not operator gives apposite of the expression
print(not(a==20 and b==30))
print(not(a==20 or b==30))
print(not(a==21 or b==31))  
print(not(a==29 and b==30))


